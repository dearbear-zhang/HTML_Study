function onLoginIn() {
    var account = document.getElementById("account").value;
    var psd = document.getElementById("pwd").value;
    if (account.length == 0){
        alert("请输入账号");
        return
    }
    if (psd.length == 0){
        alert("请输入密码")
        return
    }

}





